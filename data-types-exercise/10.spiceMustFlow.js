function solve(startingYield) {

    let spiceCount = startingYield;
    let dayCount = 0;

    while (startingYield >= 100) {

        if (dayCount !== 0) {
            spiceCount -= 10;
            let madeSpice = spiceCount - 10;
        spiceCount += madeSpice
        }
        
    
        dayCount++

    }




}
solve(111);